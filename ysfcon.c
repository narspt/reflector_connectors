void process_signal(int sig)
{
	static uint32_t c1 = 0;
	static uint32_t c2 = 0;
	if(sig == SIGINT){
		fprintf(stderr, "\n\nShutting down link\n");
		buf[0] = 'Y';
		buf[1] = 'S';
		buf[2] = 'F';
		buf[3] = 'U';
		memcpy(&buf[4], callsign, 10);
		sendto(udp1, buf, 14, 0, (const struct sockaddr *)&host1, sizeof(host1));
		sendto(udp2, buf, 14, 0, (const struct sockaddr *)&host2, sizeof(host2));
		close(udp1);
		close(udp2);
		exit(EXIT_SUCCESS);
	}
	if(sig == SIGALRM){
		buf[0] = 'Y';
		buf[1] = 'S';
		buf[2] = 'F';
		buf[3] = 'P';
		memcpy(&buf[4], callsign, 10);
		sendto(udp1, buf, 14, 0, (const struct sockaddr *)&host1, sizeof(host1));
		sendto(udp2, buf, 14, 0, (const struct sockaddr *)&host2, sizeof(host2));
		alarm(5);
	}
#ifdef DEBUG
		fprintf(stderr, "SEND BOTH: ");
		for(int i = 0; i < 14; ++i){
			fprintf(stderr, "%02x ", buf[i]);
		}
		fprintf(stderr, "\n");
		fflush(stderr);
#endif
}

void addCCITT162(unsigned char *in, unsigned int length)
{
	union crc {
		uint16_t crc16;
		uint8_t  crc8[2U];
	};
	union crc c;

	c.crc16 = 0U;

	for (unsigned i = 0U; i < (length - 2U); i++)
		c.crc16 = (uint16_t)((c.crc8[0U]) << 8) ^ CCITT16_TABLE2[c.crc8[1U] ^ in[i]];

	c.crc16 = ~c.crc16;

	in[length - 1U] = c.crc8[0U];
	in[length - 2U] = c.crc8[1U];
}

unsigned int get_syndrome_23127(unsigned int pattern)
{
	unsigned int aux = X22;
 
	if (pattern >= X11) {
		while (pattern & MASK12) {
			while (!(aux & pattern))
				aux = aux >> 1;

			pattern ^= (aux / X11) * GENPOL;
		}
	}

	return pattern;
}

unsigned int encode24128(unsigned int data)
{
    return ENCODING_TABLE_24128[data];
}

unsigned int decode23127(unsigned int code)
{
	unsigned int syndrome = get_syndrome_23127(code);
	unsigned int error_pattern = DECODING_TABLE_23127[syndrome];

	code ^= error_pattern;

	return code >> 11;
}


unsigned int decode24128(unsigned char* bytes)
{
	unsigned int code = bytes[0U];
	code <<= 8;
	code |= bytes[1U];
	code <<= 8;
	code |= bytes[2U];

	return decode23127(code >> 1);
}

void convolution_decode(uint8_t s0, uint8_t s1)
{
  *m_dp = 0U;

  for (uint8_t i = 0U; i < NUM_OF_STATES_D2; i++) {
    uint8_t j = i * 2U;

    uint16_t metric = (BRANCH_TABLE1[i] ^ s0) + (BRANCH_TABLE2[i] ^ s1);

    uint16_t m0 = m_oldMetrics[i] + metric;
    uint16_t m1 = m_oldMetrics[i + NUM_OF_STATES_D2] + (M - metric);
    uint8_t decision0 = (m0 >= m1) ? 1U : 0U;
    m_newMetrics[j + 0U] = decision0 != 0U ? m1 : m0;

    m0 = m_oldMetrics[i] + (M - metric);
    m1 = m_oldMetrics[i + NUM_OF_STATES_D2] + metric;
    uint8_t decision1 = (m0 >= m1) ? 1U : 0U;
    m_newMetrics[j + 1U] = decision1 != 0U ? m1 : m0;

    *m_dp |= (uint64_t)((decision1) << (j + 1U)) | (uint64_t)((decision0) << (j + 0U));
  }

  ++m_dp;

  uint16_t* tmp = m_oldMetrics;
  m_oldMetrics = m_newMetrics;
  m_newMetrics = tmp;
}

void convolution_chainback(unsigned char* out, unsigned int nBits)
{
	uint32_t state = 0U;

	while (nBits-- > 0) {
		--m_dp;

		uint32_t  i = state >> (9 - K);
		uint8_t bit = (uint8_t)(*m_dp >> i) & 1;
		state = (bit << 7) | (state >> 1);

		WRITE_BIT1(out, nBits, bit != 0U);
	}
}

void convolution_encode(const unsigned char* in, unsigned char* out, unsigned int nBits)
{
	uint8_t d1 = 0U, d2 = 0U, d3 = 0U, d4 = 0U;
	uint32_t k = 0U;
	for (unsigned int i = 0U; i < nBits; i++) {
		uint8_t d = READ_BIT1(in, i) ? 1U : 0U;

		uint8_t g1 = (d + d3 + d4) & 1;
		uint8_t g2 = (d + d1 + d2 + d4) & 1;

		d4 = d3;
		d3 = d2;
		d2 = d1;
		d1 = d;

		WRITE_BIT1(out, k, g1 != 0U);
		k++;

		WRITE_BIT1(out, k, g2 != 0U);
		k++;
	}
}


void fich_decode(const unsigned char* bytes)
{
	memset(m_metrics1, 0x00U, NUM_OF_STATES * sizeof(uint16_t));
	memset(m_metrics2, 0x00U, NUM_OF_STATES * sizeof(uint16_t));

	m_oldMetrics = m_metrics1;
	m_newMetrics = m_metrics2;
	m_dp = m_decisions;

	// Deinterleave the FICH and send bits to the Viterbi decoder
	for (unsigned int i = 0U; i < 100U; i++) {
		unsigned int n = INTERLEAVE_TABLE[i];
		uint8_t s0 = READ_BIT1(bytes, n) ? 1U : 0U;

		n++;
		uint8_t s1 = READ_BIT1(bytes, n) ? 1U : 0U;

		convolution_decode(s0, s1);
	}

	unsigned char output[13U];
	convolution_chainback(output, 96U);

	unsigned int b0 = decode24128(output + 0U);
	unsigned int b1 = decode24128(output + 3U);
	unsigned int b2 = decode24128(output + 6U);
	unsigned int b3 = decode24128(output + 9U);

	m_fich[0U] = (b0 >> 4) & 0xFFU;
	m_fich[1U] = ((b0 << 4) & 0xF0U) | ((b1 >> 8) & 0x0FU);
	m_fich[2U] = (b1 >> 0) & 0xFFU;
	m_fich[3U] = (b2 >> 4) & 0xFFU;
	m_fich[4U] = ((b2 << 4) & 0xF0U) | ((b3 >> 8) & 0x0FU);
	m_fich[5U] = (b3 >> 0) & 0xFFU;

	return;//CCRC::checkCCITT162(m_fich, 6U);
}

void fich_encode(unsigned char* bytes)
{
	addCCITT162(m_fich, 6U);

	unsigned int b0 = ((m_fich[0U] << 4) & 0xFF0U) | ((m_fich[1U] >> 4) & 0x00FU);
	unsigned int b1 = ((m_fich[1U] << 8) & 0xF00U) | ((m_fich[2U] >> 0) & 0x0FFU);
	unsigned int b2 = ((m_fich[3U] << 4) & 0xFF0U) | ((m_fich[4U] >> 4) & 0x00FU);
	unsigned int b3 = ((m_fich[4U] << 8) & 0xF00U) | ((m_fich[5U] >> 0) & 0x0FFU);

	unsigned int c0 = encode24128(b0);
	unsigned int c1 = encode24128(b1);
	unsigned int c2 = encode24128(b2);
	unsigned int c3 = encode24128(b3);

	unsigned char conv[13U];
	conv[0U]  = (c0 >> 16) & 0xFFU;
	conv[1U]  = (c0 >> 8) & 0xFFU;
	conv[2U]  = (c0 >> 0) & 0xFFU;
	conv[3U]  = (c1 >> 16) & 0xFFU;
	conv[4U]  = (c1 >> 8) & 0xFFU;
	conv[5U]  = (c1 >> 0) & 0xFFU;
	conv[6U]  = (c2 >> 16) & 0xFFU;
	conv[7U]  = (c2 >> 8) & 0xFFU;
	conv[8U]  = (c2 >> 0) & 0xFFU;
	conv[9U]  = (c3 >> 16) & 0xFFU;
	conv[10U] = (c3 >> 8) & 0xFFU;
	conv[11U] = (c3 >> 0) & 0xFFU;
	conv[12U] = 0x00U;

	//CYSFConvolution convolution;
	unsigned char convolved[25U];
	convolution_encode(conv, convolved, 100U);

	unsigned int j = 0U;
	for (unsigned int i = 0U; i < 100U; i++) {
		unsigned int n = INTERLEAVE_TABLE[i];

		bool s0 = READ_BIT1(convolved, j) != 0U;
		j++;

		bool s1 = READ_BIT1(convolved, j) != 0U;
		j++;

		WRITE_BIT1(bytes, n, s0);

		n++;
		WRITE_BIT1(bytes, n, s1);
	}
}

void fich_set_fi(unsigned char fi)
{
	m_fich[0U] &= 0x3FU;
	m_fich[0U] |= (fi << 6) & 0xC0U;
}

void fich_set_cs(unsigned char cs)
{
	m_fich[0U] &= 0xCFU;
	m_fich[0U] |= (cs << 4) & 0x30U;
}

void fich_set_cm(unsigned char cm)
{
	m_fich[0U] &= 0xF3U;
	m_fich[0U] |= (cm << 2) & 0x0CU;
}

void fich_set_fn(unsigned char fn)
{
	m_fich[1U] &= 0xC7U;
	m_fich[1U] |= (fn << 3) & 0x38U;
}

void fich_set_ft(unsigned char ft)
{
	m_fich[1U] &= 0xF8U;
	m_fich[1U] |= ft & 0x07U;
}

void fich_set_mr(unsigned char mr)
{
	m_fich[2U] &= 0xC7U;
	m_fich[2U] |= (mr << 3) & 0x38U;
}

void fich_set_voip(bool on)
{
	if (on)
		m_fich[2U] |= 0x04U;
	else
		m_fich[2U] &= 0xFBU;
}

int main(int argc, char **argv)
{
	struct sockaddr_in rx;
	struct hostent *hp;
	char *	host1_url;
	char *	host2_url;
	int 	host1_port;
	int 	host2_port;
	socklen_t l = sizeof(host1);
	int rxlen;
	int len = 0;
	int reuse = 1;
	int r;
	int udprx,maxudp;
	
	static uint64_t ping_cnt = 0;
	uint16_t streamid = 0;
	
	uint8_t connect_status;
	
	if(argc != 4){
		fprintf(stderr, "Usage: ysfcon [CALLSIGN] [YSFHost1IP:PORT] [YSFHost2IP:PORT]\n");
		return 0;
	}
	else{
		memset(callsign, ' ', 10);
		memcpy(callsign, argv[1], strlen(argv[1]));
		host1_url = strtok(argv[2], ":");
		host1_port = atoi(strtok(NULL, ":"));
		host2_url = strtok(argv[3], ":");
		host2_port = atoi(strtok(NULL, ":"));
		printf("YSF1: %s:%d\n", host1_url, host1_port);
		printf("YSF2: %s:%d\n", host2_url, host2_port);
	}
	

	signal(SIGINT, process_signal); 						//Handle CTRL-C gracefully
	signal(SIGALRM, process_signal); 						//Watchdog
	
	if ((udp1 = socket(AF_INET, SOCK_DGRAM, 0)) < 0) {
		perror("cannot create socket\n");
		return 0;
	}
	if ((udp2 = socket(AF_INET, SOCK_DGRAM, 0)) < 0) {
		perror("cannot create socket\n");
		return 0;
	}
	
	maxudp = max(udp1, udp2) + 1;
	
	memset((char *)&host1, 0, sizeof(host1));
	host1.sin_family = AF_INET;
	host1.sin_port = htons(host1_port);
	
	memset((char *)&host2, 0, sizeof(host2));
	host2.sin_family = AF_INET;
	host2.sin_port = htons(host2_port);
	
	hp = gethostbyname(host1_url);
	if (!hp) {
		fprintf(stderr, "could not resolve %s\n", host1_url);
		return 0;
	}
	memcpy((void *)&host1.sin_addr, hp->h_addr_list[0], hp->h_length);
	
	hp = gethostbyname(host2_url);
	if (!hp) {
		fprintf(stderr, "could not resolve %s\n", host2_url);
		return 0;
	}
	memcpy((void *)&host2.sin_addr, hp->h_addr_list[0], hp->h_length);
	host1_cnt = 0;
	host2_cnt = 0;
	host1_connect = 1;
	host2_connect = 1;
	alarm(5);
	
	while (1) {
		if(host1_connect){
			host1_connect = 0;
			buf[0] = 'Y';
			buf[1] = 'S';
			buf[2] = 'F';
			buf[3] = 'P';
			memcpy(&buf[4], callsign, 10);
			sendto(udp1, buf, 14, 0, (const struct sockaddr *)&host1, sizeof(host1));
			fprintf(stderr, "Connecting to YSF1...\n");
		}
		if(host2_connect){
			host2_connect = 0;
			buf[0] = 'Y';
			buf[1] = 'S';
			buf[2] = 'F';
			buf[3] = 'P';
			memcpy(&buf[4], callsign, 10);
			sendto(udp2, buf, 14, 0, (const struct sockaddr *)&host2, sizeof(host2));
			fprintf(stderr, "Connecting to YSF2...\n");
		}
		FD_ZERO(&udpset);
		FD_SET(udp1, &udpset);
		FD_SET(udp2, &udpset);
		r = select(maxudp, &udpset, NULL, NULL, NULL);
		//fprintf(stderr, "Select returned r == %d\n", r);
		rxlen = 0;
		if(r > 0){
			if(FD_ISSET(udp1, &udpset)) {
				rxlen = recvfrom(udp1, buf, BUFSIZE, 0, (struct sockaddr *)&rx, &l);
				udprx = udp1;
			}
			else if(FD_ISSET(udp2, &udpset)) {
				rxlen = recvfrom(udp2, buf, BUFSIZE, 0, (struct sockaddr *)&rx, &l);
				udprx = udp2;
			}
		}
#ifdef DEBUG
		if(rxlen){
			if(rx.sin_addr.s_addr == host1.sin_addr.s_addr){
			fprintf(stderr, "RECV YSF1: ");
			}
			else if(rx.sin_addr.s_addr == host2.sin_addr.s_addr){
				fprintf(stderr, "RECV YSF2: ");
			}
			for(int i = 0; i < rxlen; ++i){
				fprintf(stderr, "%02x ", buf[i]);
			}
			fprintf(stderr, "\n");
			fflush(stderr);
		}
#endif
		if(rxlen == 155){
			fich_decode(&buf[40]);
			fich_set_voip(false);
			fich_encode(&buf[40]);
			if( (udprx == udp1) && (rx.sin_addr.s_addr == host1.sin_addr.s_addr) ){
				sendto(udp2, buf, rxlen, 0, (const struct sockaddr *)&host2, sizeof(host2));
#ifdef DEBUG
				fprintf(stderr, "SEND YSF2: ");
				for(int i = 0; i < rxlen; ++i){
					fprintf(stderr, "%02x ", buf[i]);
				}
				fprintf(stderr, "\n");
				fflush(stderr);
#endif
			}
			else if( (udprx == udp2) && (rx.sin_addr.s_addr == host2.sin_addr.s_addr) ){
				sendto(udp1, buf, rxlen, 0, (const struct sockaddr *)&host1, sizeof(host1));
#ifdef DEBUG
				fprintf(stderr, "SEND YSF1: ");
				for(int i = 0; i < rxlen; ++i){
					fprintf(stderr, "%02x ", buf[i]);
				}
				fprintf(stderr, "\n");
				fflush(stderr);
#endif
			}
		}
	}
}
